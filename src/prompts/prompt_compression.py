from langchain_core.prompts import PromptTemplate
from langchain_core.prompts.few_shot import FewShotPromptTemplate

template = """
다음은 뉴스 기사 본문입니다. 아래 기사에서 **'환경', '친환경', '탄소중립', '재활용', '지속 가능성', '인증', '자원 절약' 등과 관련된 마케팅 표현이나 제품 설명**을 중심으로, 주장과 근거가 드러난 **핵심 문장만 간결하게 요약**해 주세요.


[요약 조건]
- 친환경성 또는 지속 가능성에 대한 **주장과 그에 대한 수치, 인증, 기관명 등 구체적 근거가 함께 있는 문장**은 원문 그대로 사용하는 것이 좋습니다.
- 단순한 **광고성 문장, 마케팅 문구, 제품 설명**도 반드시 포함해 주세요. 그린워싱 판단에 중요합니다.
- **요약은 최대 5문장 이내**로 하되, 환경 관련 핵심 내용만 간결하게 포함해 주세요.
- 기사 전체가 아닌, **그린워싱 여부 판단에 필요한 환경 관련 문장만 추출**하세요.

------------------------------
[기사 본문]
{news}

------------------------------
[요약된 환경 관련 핵심 문장]
"""

base_prompt = PromptTemplate(
    template=template,
    input_variables=["news"],
)
